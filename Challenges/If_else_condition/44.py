# Take a day number and display the name of that day.

day_name = int(input("Enter the number of day:"))

if day_name == 0:
        print("monday")
   
elif day_name == 1:
        print("Tuesdayday")

if day_name == 2:
       print("Wednesday")
    
if day_name == 3:
         print("Thursday")

if day_name == 4:
        print("Friday")

elif day_name == 5:
        print("Saturday")
else:
        print("Invalid day")


